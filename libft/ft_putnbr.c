#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		ft_putchar('-');
	}
	if (n >= 10 || n <= -10)
		ft_putnbr((n < 0) ? (n / 10) * -1 : n / 10);
	ft_putchar(((n < 0) ? (n % 10) * -1 : n % 10) + '0');
}
