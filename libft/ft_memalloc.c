#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*ptr;

	ptr = malloc(size);
	if (!ptr)
		return (NULL);
	while (size > 0)
	{
		size--;
		*(unsigned char*)(ptr + size) = 0;
	}
	return (ptr);
}
